DROP DATABASE IF EXISTS `Blog`;

CREATE DATABASE `Blog`;

USE `Blog`;

CREATE TABLE `User`(
	`User_id` INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	`Username` VARCHAR(50) NOT NULL,
	`Vorname` VARCHAR(30) NOT NULL,
	`Nachname` VARCHAR(30) NOT NULL,
	`Strasse` VARCHAR(50) NOT NULL,
	`PLZ` INT(4) NOT NULL,
	`Ort` VARCHAR(50) NOT NULL,
	`Email` VARCHAR(100) NOT NULL,
	`Passwort` VARCHAR(255) NOT NULL
	);

CREATE TABLE `Inhalt`(
	`Inhalt_id` INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	`Titel` VARCHAR(100) NOT NULL,
	`Text` TEXT NOT NULL,
	`Bild_URL` VARCHAR(255), 
	`Video_URL` VARCHAR(255)
);

CREATE TABLE `Beitrag`(
	`Beitrag_id` INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	`User_id` INT UNSIGNED NOT NULL,
	`Inhalt_id` INT UNSIGNED NOT NULL,
	`Zeitstempel` TIMESTAMP NOT NULL,
	FOREIGN KEY (`User_id`) REFERENCES `User` (`User_id`),
	FOREIGN KEY (`Inhalt_id`) REFERENCES `Inhalt` (`Inhalt_id`)
);